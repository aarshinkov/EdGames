package obj;

public class AuxDate {
    private int day;
    private int month;
    private int year;

    private int subYearOne;
    private int subYearTwo;

    public AuxDate() {
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getSubYearOne() {
        return subYearOne;
    }

    public void setSubYearOne(int subYearOne) {
        this.subYearOne = subYearOne;
    }

    public int getSubYearTwo() {
        return subYearTwo;
    }

    public void setSubYearTwo(int subYearTwo) {
        this.subYearTwo = subYearTwo;
    }
}
